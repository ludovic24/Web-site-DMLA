	<div id='contenuDmla'>
	
	 <p>La Dégénérescence Maculaire Liée à l’Age ou « DMLA » est une atteinte de la macula.<br /><br />

La macula est la zone centrale de la rétine permettant la vision fine (ou centrale), c’est-à-dire par exemple la lecture, la reconnaissance des détails, des visages, la conduite automobile,… Cette localisation centrale explique que la DMLA peut entraîner une tache floue au centre du champ visuel, mais qu’elle laisse habituellement intacte la vision périphérique ou latérale.<br /><br />

Cette maladie est aujourd’hui la première cause de handicap visuel chez les personnes âgées de plus de 50 ans dans les pays développés, atteignant plus d’un million d’individus en France et des centaines de millions à travers le monde. Le risque de survenue de la maladie augmente avec l’âge pour dépasser 25% de la population après l’âge de 75 ans.<br /><br />

L’apparition d’une tache centrale (le scotome) et/ou sensation de voir les lignes droites déformées, gondolées doivent amener à consulter le plus rapidement possible un ophtalmologiste, qui seul pourra poser le diagnostic d’un DMLA.</p> </div>

