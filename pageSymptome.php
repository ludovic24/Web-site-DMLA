<h2>Quels sont les symptômes ?</h2>
<p>Au stade initial de Maculopathie Liée à l’Âge, dans la très grande majorité des cas, aucun symptôme n’est ressenti pendant de nombreuses années. Seul un examen du fond d’œil pratiqué par un ophtalmologiste permet de la diagnostiquer.

Ensuite, l’acuité visuelle diminue et les personnes ressentent le besoin d’un meilleur éclairage pour lire.

Aux stades plus avancés, apparaît la sensation d’une tache au centre du champ visuel : les personnes ont une bonne vision d’ensemble d’une scène mais ne peuvent pas en saisir les détails.

Il y a caractère d’urgence à consulter l’ophtalmologiste en cas de baisse rapide de la vision d’un des deux yeux et/ou sensation de voir les lignes droites déformées, gondolées.</p><br /><br />

<h2>Quels sont les premiers symptômes qui doivent alerter ?</h2>
<p>
• Une baisse d’acuité visuelle rapide.<br />

• Une vision déformée ou gondolée.<br />

• Une tâche centrale (scotome).<br />

</p>
