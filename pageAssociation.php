    <p> Age-related Macular Degeneration or "AMD" is an attack of the macula. <br /> <br />

The macula is the central area of ​​the retina allowing fine vision (or central), that is to say for example reading, recognition of details, faces, driving, ... This central location explains that AMD may cause a blur in the center of the visual field, but usually leaves peripheral or lateral vision intact. <br /> <br />

This disease is today the leading cause of visual impairment in people over 50 in developed countries, reaching more than one million individuals in France and hundreds of millions around the world. The risk of occurrence of the disease increases with age to exceed 25% of the population after the age of 75. <br /> <br />

The appearance of a central spot (the scotoma) and / or the sensation of seeing distorted, curled straight lines must prompt consultation with an ophthalmologist, who alone can make the diagnosis of AMD. </p>
	
