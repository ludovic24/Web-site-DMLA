	<h2>Les Précurseurs (stade initial, ou Maculopathie Liée à l’Age)</h2>

<p>Ils peuvent être observés à l’examen du fond d’œil avant l’apparition des signes de la maladie proprement dite, souvent même avant les premiers symptômes. Les précurseurs les plus caractéristiques sont les « drusen » (accumulation de dépôts sur la rétine) et les anomalies de l’épithélium pigmentaire rétinien. Cette situation n’évolue pas obligatoirement vers une forme plus sévère.</p><br /><br />

<h2>La forme « Atrophique » ou « Sèche »</h2>
<p>Dans ce cas, une perturbation du métabolisme cellulaire entraîne progressivement une dégénérescence atrophique des cellules rétiniennes. Les premiers signes peuvent être une gêne à la lecture ou à l’écriture, ou une baisse progressive de l’acuité visuelle. Cette forme évolue en général lentement, permettant aux patients atteints de conserver longtemps une vision relativement satisfaisante, malgré une gêne pour les activités nécessitant la reconnaissance des détails.</p><br /><br />

<h2>La forme « Exsudative » ou « Néovasculaire » dite « Humide »</h2>

<p>Cette forme se caractérise par le développement de vaisseaux dits « néovaisseaux » dans la région maculaire. Ces vaisseaux anormaux sont fragiles et laissent diffuser du sérum, entraînant un décollement séreux, ou du sang, entraînant une hémorragie. L’évolution dépendra du type et de la localisation de ces vaisseaux anormaux, de la survenue éventuelle de soulèvements rétiniens, d’hémorragies, et de la réponse aux traitements. Les premiers signes d’alerte – déformations des lignes droites, diminution brutale d’acuité visuelle ou des contrastes, tache sombre centrale – doivent entraîner une consultation rapide par un médecin ophtalmologiste. Il est en effet très important de détecter les premiers signes d’une dégénérescence maculaire exsudative, car les traitements sont d’autant plus efficaces que le diagnostic est posé tôt. </p> </div>

